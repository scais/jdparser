package cz.zcu.validator.jdparser.api.javadocs.modifiers;

/**
 * Holds String representation of all possible JavaDoc's modifiers. These
 * constatns are used during parsing JavaDoc's elements.
 * 
 * @author Stepan Cais, scais@kiv.zcu.cz, 7.12.2012
 */
public class JavaDocModifiersUtil {

    /**
     * String representation for {@code author} annotation.
     */
    public static final String AUTHOR = "author";

    /**
     * String representation for {@code depraceted} annotation.
     */
    public static final String DEPRACETED = "depraceted";

    /**
     * String representation for {@code return} annotation.
     */
    public static final String RETURN = "return";

    /**
     * String representation for {@code see} annotation.
     */
    public static final String SEE = "see";

    /**
     * String representation for {@code since} annotation.
     */
    public static final String SINCE = "since";

    /**
     * String representation for {@code version} annotation.
     */
    public static final String VERSION = "version";

}
